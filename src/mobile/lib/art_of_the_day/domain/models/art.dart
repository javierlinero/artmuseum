class Artwork {
  final String title;
  final List<String> artists;
  final String imageUrl;
  final String year;
  final String materials;
  final String size;
  final String description;

  Artwork({
    required this.title,
    required this.artists,
    required this.imageUrl,
    required this.year,
    required this.materials,
    required this.size,
    required this.description,
  });
}
